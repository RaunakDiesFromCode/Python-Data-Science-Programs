import math

# a. Calculate square root
number = 16
sq = math.sqrt(number)
print(f"Square root of {number}: {sq}")

# b. Calculate factorial
number = 5
fact = math.factorial(number)
print(f"Factorial of {number}: {fact}")

# c. Calculate power
x = 2
y = 3
pow = math.pow(x, y)
print(f"{x} raised to the power {y}: {pow}")

# d. Calculate greatest common divisor
a = 54
b = 24
gcd_value = math.gcd(a, b)
print(f"Greatest common divisor of {a} and {b}: {gcd_value}")

# e. Calculate ceiling and floor
floating_number = 4.7
ceiling = math.ceil(floating_number)
floor = math.floor(floating_number)
print(f"Ceiling of {floating_number}: {
      ceiling}, Floor of {floating_number}: {floor}")
